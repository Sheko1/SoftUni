day = int(input())

if 1 <= day <= 7:
    if day == 1:
        print("Monday")

    elif day == 2:
        print("Tuesday")

    elif day == 3:
        print("Wednesday")

    elif day == 4:
        print("Thursday")

    elif day == 5:
        print("Friday")

    elif day == 6:
        print("Saturday")

    elif day == 7:
        print("Sunday")

else:
    print("Error")
